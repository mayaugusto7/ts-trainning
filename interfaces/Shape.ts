interface Shape {
    color: string;
}

interface PenStroke {
    penWidth: number;
}

interface Square extends Shape, PenStroke {
    sideLength: number;
}

let square = {} as Square;
square.color = "blue";
square.sideLength = 10;
square.penWidth = 5.0;

console.log('interface Square extends Shape, PenStroke')
console.log('Extend interfaces and multiple interfaces...');
console.log(square);

